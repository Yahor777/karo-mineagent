// Фаза 1 («не забывать ничего»): модель данных живой памяти проекта.
//
// Память проекта хранится в одном человекочитаемом файле .mineagent/project.md.
// Файл ведётся СОВМЕСТНО: часть разделов агент пишет сам (identity, журнал задач),
// часть наполняется по ходу работы (конвенции, контент, решения, открытые вопросы),
// и пользователь может править их вручную — агент сохраняет эти правки.
//
// Принципиально: память провайдеро-НЕЗАВИСИМА. Это просто текст, который
// подмешивается в начало контекста при каждом запросе. Не требует embeddings,
// поэтому работает с kimchi (custom), Cloudflare, Fireworks, WaveSpeed и любым
// OpenAI-совместимым провайдером одинаково.

// Факты идентичности проекта. Берутся из ProjectMap (repoIndexer) при каждом run
// и записываются в auto-раздел project.md, чтобы агент всегда «помнил» базу,
// даже если индексатор временно недоступен или контекст был сжат.
export interface ProjectIdentityFacts {
  loader?: string;
  minecraftVersion?: string;
  javaVersion?: string;
  mainModId?: string;
  registriesCount?: number;
  eventHandlersCount?: number;
  updatedAt?: string;
}

// Разделы памяти, которые наполняются по ходу проекта. Каждый раздел —
// список коротких фактов/строк. Агент дописывает, пользователь редактирует.
export type MemorySectionId = "conventions" | "content" | "decisions" | "open";

// Одна запись журнала задач. Пишется автоматически после каждого run, чтобы
// сохранить «что делали и чем закончилось» навсегда, даже когда история чата
// будет сжата (summarization, Фаза 1 п.4).
export interface RunLogEntry {
  at: string;       // ISO timestamp
  mode: string;     // ask | plan | build | playtest
  task: string;     // запрос пользователя (обрезанный)
  summary: string;  // краткий итог ответа модели (обрезанный)
}

// Опции рендера памяти в промпт. Позволяют ограничить объём, чтобы не жечь
// токены: журнал задач показывается только последними N записями.
export interface MemoryRenderOptions {
  // Максимум записей журнала задач в контексте (свежие — первыми). По умолчанию 8.
  maxLogEntries?: number;
  // Жёсткий потолок длины блока памяти в символах. По умолчанию 6000.
  maxChars?: number;
}
